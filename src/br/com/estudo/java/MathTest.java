package br.com.estudo.java;
public class MathTest {
    public static void main(String[] args) {
        System.out.println("seno: " +Math.sin(10)); //seno
        System.out.println("cosseno: " +Math.cos(10)); // cosseno
        System.out.println("tangente: " +Math.sin(45)); // tan
        System.out.println("potencia: " +Math.pow(4,2)); // potencia
        System.out.println("raiz quadrada: " +Math.sqrt(16)); // raiz quadrada
        System.out.println("raiz cubica: " +Math.cbrt(16)); // raiz cubica
        System.out.println("hipotenusa: " +Math.hypot(6,8)); // hipotenusa
        System.out.println("arco seno de v -1 ate 1: " +Math.asin(10)); // arco seno de v -1 ate 1
        System.out.println("arco cosseno: " +Math.acos(10)); // arco cosseno
        System.out.println("arco tangente: " +Math.atan(10)); // arco tangente
        System.out.println("seno hiperbolico de x: " +Math.sinh(10)); // seno hiperbolico de x
        System.out.println("elevado: " +Math.pow(2,3));// elevado
        System.out.println("1 positivo 0 igual -1 negativo: " +Math.signum(10)); // 1 positivo 0 igual -1 negativp
        System.out.println("maior inteiro: " +Math.ceil(3.7)); // maior inteiro
        System.out.println("menor inteiro: " +Math.floor(3.7)); // menor inteiro
        System.out.println("numero arredondado: " +Math.round(10.50)); // numero arredondado
        System.out.println("retorna o maior: " +Math.max(10,12));// retorna o maior
        System.out.println("retorna maior: " +Math.min(10,13)); //retorna o menor
       
    }
    
}